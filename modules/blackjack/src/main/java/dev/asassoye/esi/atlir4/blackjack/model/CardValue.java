package dev.asassoye.esi.atlir4.blackjack.model;

import dev.asassoye.esi.atlir4.cards.Card;
import dev.asassoye.esi.atlir4.cards.Value;

public class CardValue {
    public static short weight(Card card) {
        Value value = card.getValue();

        switch (value) {
            case ACE:
                return 11;
            case TWO:
                return 2;
            case THREE:
                return 3;
            case FOUR:
                return 4;
            case FIVE:
                return 5;
            case SIX:
                return 6;
            case SEVEN:
                return 7;
            case EIGHT:
                return 8;
            case NINE:
                return 9;
            case TEN:
            case JACK:
            case QUEEN:
            case KING:
            default:
                return 10;
        }
    }
}
